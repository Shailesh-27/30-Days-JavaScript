//Task 8: Write a program to compare two numbers using > and < and log the result to the console

let num1 = 10
let num2 = 20

console.log(num1 > num2);    // 10 > 20 which is not true
console.log(num1 < num2);    // 10 < 20 which is true

//Task 9: Write a program to compare two numbers using >= and <= and log the result to the console

let num3 = 10
let num4 = 10

console.log(num3 >= num4);    // 20 >= 20 
console.log(num3 <= num4);    // 20 <= 20


//Task 10: Write a program to compare two numbers using == and === and log the result to the console


let num5 = 10
let num6 = '10'

console.log(num5 == num6);    // == only check the value of variable
console.log(num5 === num6);   // === It Also check datatype of the variable

console.log(10 === 10);
